// Get all countries for gameplay
export function getAllCountries(): Country[] {
  return WORLD_COUNTRIES;
}

// Get popular countries for easier gameplay
export function getPopularCountries(): Country[] {
  return WORLD_COUNTRIES.filter(country => POPULAR_COUNTRIES.includes(country.id));
}

// Find country by name (case insensitive)
export function findWorldCountryByName(name: string): Country | null {
  return WORLD_COUNTRIES.find(country =>
    country.name.toLowerCase() === name.toLowerCase() ||
    country.name.toLowerCase().includes(name.toLowerCase())
  ) || null;
}

// Get random country from the full world set
export function getRandomWorldCountry(): Country {
  return WORLD_COUNTRIES[Math.floor(Math.random() * WORLD_COUNTRIES.length)];
}

// Get random popular country (easier for gameplay)
export function getRandomPopularCountry(): Country {
  const popularCountries = getPopularCountries();
  return popularCountries[Math.floor(Math.random() * popularCountries.length)];
}